package com.unocareer;
public class SwitchCase {
	public static void main(String[] args) {

		long number=2;

		switch((int)number) {

		default :
			System.out.println("print default");
			break;
		case 2:
			System.out.println("print 2");
			break;
		case 3:
			System.out.println("print 3");
			break;
		case 1:
			System.out.println("print 1");
		}
	}
}
